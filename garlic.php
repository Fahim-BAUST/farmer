	Garlic<br>Introduction<br>

Garlic, a native of Southern Europe is one of the important bulb crops grown and used as a spice or condiment throughout India. Gujarat followed by Orissa are the largest producing states. It possesses a high natritive value, its preparations are administered as a cure against stomach disease, sore eyes and ear ache. It is commonly used in the preparation of various dishes. Allicin, the principle amoebic dysentry and is also having many other medicinal properties.


<br>

 Garlic Cultivation<br>

Climate and Soil<br>
It is grown under a wide range of climatic conditions. However, it cannot stand too hot or too cold weather. It prefers moderate temperature in summer as well as in winter. Short days are very favourable for the formation of bulbs. It can be grown well at elevations of 1000 to 1300 m above MSL. Garlic requires well drained loamy soils, rich in humus, with fairly good content of potash. The crop raised on sandy or loose soil does soils, the bulbs produced are deformed and during harvesting, many bulbs are broken and bruised and so they do not keep well in storage.
<br>
Varieties<br>
There is no distinct variety of garlic. Local varieties are either white in colour and have fairly big bulbs with a better keeping quality and a higher yield or red in colour with pungency. Tamil Nadu Agricultural University has recently released one improved variety by clonal selection viz., Ooty 1 Garlic. It is a high yielder (17t/ha) with a shorter duration of 120 to 130 days. The bulbs are big sized weighing 20 to 30g and each bulb has 22 to 25 cloves, which are dull white in colour.
<br>
Propagation<br>
Garlic is propagated by cloves. All the cloves are planted except the long slender once in the centre of the bulb. Bulbs with side growth should be discarded. Healthy cloves or bulbils free from disease and injuries should be used for sowing and about 150 to 200 kg cloves are required to plant one hectare. They are sown by dibbling or furrow planting.
<br>
Dibbling: The field is divided into small plots convenient for irrigation Cloves may be dibbled 5 to 7.5cm deep, keeping their growing ends upwards. They are laced 7.5cm apart from each other in rows of 15cm apart and then they are covered with loose soil. June-July and October-November are the normal planting seasons for garlic.
Furrow planting: The furrows are made 15 cm with hand how or a cotton drill. In these furrows, cloves are dropped by hand 7.5 to 10 cm apart. They are covered lightly with loose soil and a light irrigation is given.
Manures and fertilisers
About 25 tonnes of farm yard manure is applied as a basal dose along with 60kg Nitrogen and 50 kg in each of Phosphorus and Potash. Forty five days after planting 60kg Nitrogen is applied again as top dressing.
<br>
Irrigation<br>
First irrigation is given after sowing and then field is irrigated every 10 to 15 days depending upon the soil moisture availability. There should not be any scarcity of moisture in the growing season, otherwise, the development of the bulbs will be affected. The last irrigation should be given 2 to 3 before harvesting for making it easy without damaging the bulbs. In South India hills, they are mostly grown as a rainfed crop.
<br>
Intercultural operations<br>
First interculture is given with hand hoe one month after sowing. Second weeding is given one month after the first (about two and half months from sowing) loosens the soil and helps in the setting of bigger and well filled bulbs. The crop should not be weeding out or hoed at a later stage because this may damage the stem and impair the keeping quality.
<br>
Harvesting<br>
Garlic is a crop of 4 ½ to 5 months duration. When the leaves start turning yellowish or brownish and show signs of drying up, the crop is ready for harvest. The plants are then pulled out or uprooted with a country plough and are tied into small bundles which are then kept in the field or in the shade or 2-3 days for curing and drying so that the bulbs become hard and their keeping quality is improved. The bulbs may be stored by hanging them on bamboo sticks or by keeping them on dry sand on the market, the dried stalks are removed and bulbs are cleaned. Well cured garlic bulbs can be kept for 1 to 1 ½ months in an ordinary well ventilated room. If dust smoke is given to it, the bulbs can be stored for 8 to 10 months. They can also be stored at 320F with 60% R.H. Average yield level is 6 to 8 t/ha.
<br>
Plant protection<br>
Thrips cause withering of the leaves. Application of methyl demeton 25EC 1 ml/litre will check the incidence. Leaf spot is the most important disease. Spraying Dithane M-45 at fortnightly intervals at 2.5g in one litre of water is recommended.<br>

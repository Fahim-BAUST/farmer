				<h3>Seed quality and selection</h3>
				<p class="p-3 mb-2 bg-success text-white">
				Seed is a living product that must be grown, harvested, and processed correctly in order to realize the yield potential of any rice variety. Good quality seed can increase yields by 5-20%. Using good seed leads to lower seeding rates, higher crop emergence, reduced replanting, more uniform plant stands, and more vigorous early crop growth. Vigorous growth in early stages reduces weed problems and increases crop resistance to insect pests and diseases. All of these factors contribute to higher yields and more productive rice farms.

				Good seed is pure (of the chosen variety), full and uniform in size, viable (more than 80% germination with good seedling vigor), and free of weed seeds, seed-borne diseases, pathogens, insects, or other matter.

				Choosing seed of a suitable variety of rice that suits the environment it will be grown in and ensuring the seed choosen of that variety is of the highest possible quality is the essential first step in rice production.

				</p>

				<h3>Land preparation</h3>
				<p class="p-3 mb-2 bg-success text-white">Before rice can be planted, the soil should be in the best physical condition for crop growth and the soil surface is level. Land preparation involves plowing and harrowing to ‘till’ or dig-up, mix and level the soil.

				Tillage allows the seeds to be planted at the right depth, and also helps with weed control. Farmers can till the land themselves using hoes and other equipment or they can be assisted by draft animals, such as buffalo, or tractors and other machinery.

				Next, the land is leveled to reduce the amount of water wasted by uneven pockets of too-deep water or exposed soil. Effective land leveling allows the seedlings to become established more easily, reduces the amount of effort required to manage the crop, and increases both grain quality and yields.</p>
				
				<h3>Crop establishment</h3>
				<p class="p-3 mb-2 bg-success text-white">The two main practices of establishing rice plants are transplanting and direct seeding.

				Transplanting  is the most popular plant establishment technique across Asia. Pre- germinated seedlings are transferred from a seedbed to the wet field. It requires less seed and is an effective method to control weeds, but requires more labor. Seedlings may be transplanted by either machine or hand.

				Direct seeding involves broadcasting dry seed or pre-germinated seeds and seedlings by hand or planting them by machine. In rainfed and deepwater ecosystems, dry seed is manually broadcast onto the soil surface and then incorporated either by ploughing or by harrowing while the soil is still dry. In irrigated areas, seed is normally pre- germinated prior to broadcasting.</p>
				<h3>Water use and management</h3>
				<p class="p-3 mb-2 bg-success text-white">Cultivated rice is extremely sensitive to water shortages. To ensure sufficient water, most rice farmers aim to maintain flooded conditions in their field. This is especially true for lowland rice. Good water management in lowland rice focuses on practices that conserve water while ensuring sufficient water for the crop.

				In rainfed environments when optimal amounts of water may not be available for rice production, a suite of options are available to help farmers cope with different degrees and forms of water scarcity. It includes sound land preparation and pre-planting activities followed by techniques such as saturated soil culture, alternate wetting and drying, raised beds, mulching, and use of aerobic rice that can cope with dryer conditions</p>
				<h3>Nutrient management</h3>
				<p class="p-3 mb-2 bg-success text-white">At each growth stage, the rice plant has specific nutrient needs. This makes nutrient management a critical aspect of rice farming.

				The unique properties of flooded soils make rice different from any other crop. Because of prolonged flooding in rice fields, farmers are able to conserve soil organic matter and also receive free input of nitrogen from biological sources, which means they need little or no nitrogen fertilizer to retain yields. However, farmers can tailor nutrient management to the specific conditions of their field to increase yields.</p>
				<h3>Crop health</h3>
				<p class="p-3 mb-2 bg-success text-white">The rice plant has a wide array of ‘enemies’ in the field. These include rodents, harmful insects, viruses, diseases, and weeds. Farmers manage weeds through water management and land preparation, by hand weeding, and in some cases herbcide application. Understanding the interactions among pests, natural enemies, host plants, other organisms, and the environment allows farmers to determine what if any pest management may be necessary.

				Avoiding conditions that allow pests to adapt and thrive in a particular ecosystem helps to identify weak links in the pests' life cycle and therefore what factors can be manipulated to manage them. Retaining natural ecosystems such that predators and natural enemies of pests and diseases are kept in abundance can also help keep pest numbers down.</p>
				<h3>Harvest</h3>
				<p class="p-3 mb-2 bg-success text-white">Harvesting is the process of collecting the mature rice crop from the field. Depending on the variety, a rice crop usually reaches maturity at around 105–150 days after crop establishment. Harvesting activities include cutting, stacking, handling, threshing, cleaning, and hauling. Good harvesting methods help maximize grain yield and minimize grain damage and deterioration.

				Harvesting can be done manually or mechanically:

				Manual harvesting is common across Asia It involves cutting the rice crop with simple hand tools like sickles and knives. Manual harvesting is very effective when a crop has lodged or fallen over, however it is labor intensive. Manual harvesting requires 40 to 80 hours per hectare and it takes additional labor to manually collect and haul the harvested crop.

				Mechanical harvesting using reapers or combine harvesters is the other option, but not so common due to the availability and cost of machinery. Following cutting the rice must be threshed to separate the grain from the stalk and cleaned. These processes can also be done by hand or machine.</p>

				

			

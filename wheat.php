Physiology<br>
Leaves emerge from the shoot apical meristem in a telescoping fashion until the transition to reproduction ie. flowering.[32] The last leaf produced by a wheat plant is known as the flag leaf. It is denser and has a higher photosynthetic rate than other leaves, to supply carbohydrate to the developing ear. In temperate countries the flag leaf, along with the second and third highest leaf on the plant, supply the majority of carbohydrate in the grain and their condition is paramount to yield formation.[33][34] Wheat is unusual among plants in having more stomata on the upper (adaxial) side of the leaf, than on the under (abaxial) side.[35] It has been theorised that this might be an effect of it having been domesticated and cultivated longer than any other plant.[36] Winter wheat generally produces up to 15 leaves per shoot and spring wheat up to 9[37] and winter crops may have up to 35 tillers (shoots) per plant (depending on cultivar).[37]
<br>
Wheat roots are among the deepest of arable crops, extending as far down as 2m.[38] While the roots of a wheat plant are growing, the plant also accumulates an energy store in its stem, in the form of fructans,[39] which helps the plant to yield under drought and disease pressure,[40] but it has been observed that there is a trade-off between root growth and stem non-structural carbohydrate reserves.[41] Root growth is likely to be prioritised in drought-adapted crops, while stem non-structural carbohydrate is prioritised in varieties developed for countries where disease is a bigger issue. Depending on variety, wheat may be awned or not awned. Producing awns incurs a cost in grain number,[42] but wheat awns photosynthesise more water-use-efficiently than their leaves,[43] so awns are much more frequent in varieties of wheat grown in hot drought-prone countries than those generally seen in temperate countries. For this reason, awned varieties could become more widely grown due to climate change. In Europe, however, a decline in climate resilience of wheat has been observed.[44]
<br>
Genetics and breeding
In traditional agricultural systems wheat populations often consist of landraces, informal farmer-maintained populations that often maintain high levels of morphological diversity. Although landraces of wheat are no longer grown in Europe and North America, they continue to be important elsewhere. The origins of formal wheat breeding lie in the nineteenth century, when single line varieties were created through selection of seed from a single plant noted to have desired properties. Modern wheat breeding developed in the first years of the twentieth century and was closely linked to the development of Mendelian genetics. The standard method of breeding inbred wheat cultivars is by crossing two lines using hand emasculation, then selfing or inbreeding the progeny. Selections are identified (shown to have the genes responsible for the varietal differences) ten or more generations before release as a variety or cultivar.[45]
<br>
Major breeding objectives include high grain yield, good quality, disease and insect resistance and tolerance to abiotic stresses, including mineral, moisture and heat tolerance. The major diseases in temperate environments include the following, arranged in a rough order of their significance from cooler to warmer climates: eyespot, Stagonospora nodorum blotch (also known as glume blotch), yellow or stripe rust, powdery mildew, Septoria tritici blotch (sometimes known as leaf blotch), brown or leaf rust, Fusarium head blight, tan spot and stem rust. In tropical areas, spot blotch (also known as Helminthosporium leaf blight) is also important.
<br>
Wheat has also been the subject of mutation breeding, with the use of gamma, x-rays, ultraviolet light, and sometimes harsh chemicals. The varieties of wheat created through these methods are in the hundreds (going as far back as 1960), more of them being created in higher populated countries such as China.[46] Bread wheat with high grain iron and zinc content has been developed through gamma radiation breeding,[47] and through conventional selection breeding.[48]
<br>
International wheat breeding is led by CIMMYT in Mexico. ICARDA is another major public sector international wheat breeder, but it was forced to relocate from Syria in the Syrian Civil War.[49]
<br>
Yields<br>
The presence of certain versions of wheat genes has been important for crop yields. Genes for the 'dwarfing' trait, first used by Japanese wheat breeders to produce short-stalked wheat, have had a huge effect on wheat yields worldwide, and were major factors in the success of the Green Revolution in Mexico and Asia, an initiative led by Norman Borlaug. Dwarfing genes enable the carbon that is fixed in the plant during photosynthesis to be diverted towards seed production, and they also help prevent the problem of lodging. 'Lodging' occurs when an ear stalk falls over in the wind and rots on the ground, and heavy nitrogenous fertilization of wheat makes the grass grow taller and become more susceptible to this problem. By 1997, 81% of the developing world's wheat area was planted to semi-dwarf wheats, giving both increased yields and better response to nitrogenous fertilizer.[citation needed]
<br>
The world record wheat yield is about 17t/ha, reached in New Zealand in 2017.[50] A project in the UK, led by Rothamsted Research has aimed to raise wheat yields in the country to 20t/ha by 2020, but in 2018 the UK record stood at 16t/ha, and the average yield was just 8t/ha.[51][52]
<br>
Disease resistance<br>
Wild grasses in the genus Triticum and related genera, and grasses such as rye have been a source of many disease-resistance traits for cultivated wheat breeding since the 1930s.[53]
<br>
Hybrid wheats<br>
Because wheat self-pollinates, creating hybrid varieties is extremely labor-intensive; the high cost of hybrid wheat seed relative to its moderate benefits have kept farmers from adopting them widely[54][55] despite nearly 90 years of effort.[56]
<br>
F1 hybrid wheat cultivars should not be confused with wheat cultivars deriving from standard plant breeding. Heterosis or hybrid vigor (as in the familiar F1 hybrids of maize) occurs in common (hexaploid) wheat, but it is difficult to produce seed of hybrid cultivars on a commercial scale as is done with maize because wheat flowers are perfect in the botanical sense, meaning they have both male and female parts, and normally self-pollinate.[45] Commercial hybrid wheat seed has been produced using chemical hybridizing agents, plant growth regulators that selectively interfere with pollen development, or naturally occurring cytoplasmic male sterility systems. Hybrid wheat has been a limited commercial success in Europe (particularly France), the United States and South Africa.[57]
<br>
Synthetic hexaploids made by crossing the wild goatgrass wheat ancestor Aegilops tauschii, and various other Aegilops,[58] and various durum wheats are now being deployed, and these increase the genetic diversity of cultivated wheats.[59][60][61]
<br>
Triticale: Wheat-rye hybrid
<br>
The smaller grain of wheat on the left, larger kernels of rye next, and triticale on the right — triticale grain is significantly larger than wheat.
In ancient times, wheat was often considered a luxury grain because it had lower yield but better taste and digestibility than competitors like rye. In the 19th century, efforts were made to hybridize the two do get a crop with the best traits of both. This produced triticale, a grain with high potential, but fraught with problems relating to fertility and germination. These have mostly been solved, so that in the 20th century millions of acres of triticale are being grown worldwide.
<br>
Gluten<br>
Modern bread wheat varieties have been cross-bred to contain greater amounts of gluten,[62] which affords significant advantages for improving the quality of breads and pastas from a functional point of view.[63] However, a 2020 study that grew and analyzed 60 wheat cultivars from between 1891 and 2010 found no changes in albumin/globulin and gluten contents over time. "Overall, the harvest year had a more significant effect on protein composition than the cultivar. At the protein level, we found no evidence to support an increased immunostimulatory potential of modern winter wheat."[64]
<br>
Water efficiency<br>
Stomata (or leaf pores) are involved in both uptake of carbon dioxide gas from the atmosphere and water vapor losses from the leaf due to water transpiration. Basic physiological investigation of these gas exchange processes has yielded valuable carbon isotope based methods that are used for breeding wheat varieties with improved water-use efficiency. These varieties can improve crop productivity in rain-fed dry-land wheat farms.[65]<br>


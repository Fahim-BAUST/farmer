Genome<br>
In 2010, a team of UK scientists funded by BBSRC announced they had decoded the wheat genome for the first time (95% of the genome of a variety of wheat known as Chinese Spring line 42).[70] This genome was released in a basic format for scientists and plant breeders to use but was not a fully annotated sequence which was reported in some of the media.[71] On 29 November 2012, an essentially complete gene set of bread wheat was published.[72] Random shotgun libraries of total DNA and cDNA from the T. aestivum cv. Chinese Spring (CS42) were sequenced in Roche 454 pyrosequencer using GS FLX Titanium and GS FLX+ platforms to generate 85 Gb of sequence (220 million reads) and identified between 94,000 and 96,000 genes.[72] The implications of the research in cereal genetics and breeding includes the examination of genome variation, analysis of population genetics and evolutionary biology, and further studying epigenetic modifications.[73] In 2018 an even more complete Chinese Spring genome was released by a different team.[74]
<br>
Then in 2020 some of the same researchers produced 15 genome sequences from various locations and varieties around the world[67][68][69] - the most complete and detailed so far[67][68][69] - along with examples of their own use of the sequences to localize particular insect and disease resistance factors.[67][68][69] The team expects these sequences will be useful in future cultivar breeding.[67][68][69]
<br>
Varieties<br>
There are around 30,000 wheat varieties of 14 species grown throughout the world. Of these about 1,000 are commercially significant. In the United States over 500 varieties are available. In Canada different varieties are blended prior to sale. "Identity preserved" wheat that has been stored and transported separately (at extra cost) usually fetches a higher price.[75]<br>
<br>
Apart from mutant versions of genes selected in antiquity during domestication, there has been more recent deliberate selection of alleles that affect growth characteristics. Some wheat species are diploid, with two sets of chromosomes, but many are stable polyploids, with four sets of chromosomes (tetraploid) or six (hexaploid).[76]
<br>
Einkorn wheat (T. monococcum) is diploid (AA, two complements of seven chromosomes, 2n=14).[4]
<br>
Most tetraploid wheats (e.g. emmer and durum wheat) are derived from wild emmer, T. dicoccoides. Wild emmer is itself the result of a hybridization between two diploid wild grasses, T. urartu and a wild goatgrass such as Aegilops searsii or Ae. speltoides. The unknown grass has never been identified among non extinct wild grasses, but the closest living relative is Aegilops speltoides.[77] The hybridization that formed wild emmer (AABB) occurred in the wild, long before domestication,[76] and was driven by natural selection.


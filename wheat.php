Hexaploid wheats evolved in farmers' fields. Either domesticated emmer or durum wheat hybridized with yet another wild diploid grass (Aegilops tauschii) to make the hexaploid wheats, spelt wheat and bread wheat.[76] These have three sets of paired chromosomes, three times as many as in diploid wheat.

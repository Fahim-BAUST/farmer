Hexaploid species
<br>
Common wheat or bread wheat (T. aestivum) – A hexaploid species that is the most widely cultivated in the world.
Spelt (T. spelta) – Another hexaploid species cultivated in limited quantities.[quantify] Spelt is sometimes considered a subspecies[by whom?] of the closely related species common wheat (T. aestivum), in which case its botanical name is considered to be T. aestivum ssp. spelta.
Tetraploid species
<br>
Durum (T. durum) – A tetraploid form of wheat widely used today, and the second most widely cultivated wheat.
Emmer (T. dicoccon) – A tetraploid species, cultivated in ancient times but no longer in widespread use.
Khorasan (T. turgidum ssp. turanicum, also called T. turanicum) is a tetraploid wheat species. It is an ancient grain type; Khorasan refers to a historical region in modern-day Afghanistan and the northeast of Iran. This grain is twice the size of modern-day wheat and is known for its rich nutty flavor.
Diploid species
<br>
Einkorn (T. monococcum) – A diploid species with wild and cultivated variants. Domesticated at the same time as emmer wheat.
Hulled versus free-threshing species[edit]
<br>
Left: Naked wheat, Bread wheat Triticum aestivum; Right: Hulled wheat, Einkorn, Triticum monococcum. Note how the einkorn ear breaks down into intact spikelets.
The four wild species of wheat, along with the domesticated varieties einkorn,[78] emmer[79] and spelt,[80] have hulls. This more primitive morphology (in evolutionary terms) consists of toughened glumes that tightly enclose the grains, and (in domesticated wheats) a semi-brittle rachis that breaks easily on threshing.
<br>
The result is that when threshed, the wheat ear breaks up into spikelets. To obtain the grain, further processing, such as milling or pounding, is needed to remove the hulls or husks. Hulled wheats are often stored as spikelets because the toughened glumes give good protection against pests of stored grain.[78]
<br>
In free-threshing (or naked) forms, such as durum wheat and common wheat, the glumes are fragile and the rachis tough. On threshing, the chaff breaks up, releasing the grains.[citation needed]
